#include "../include/libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;

	i = ft_strlen((char *)s);
	while (s[i] != c && i > 0)
	{
		i--;
	}
	if (s[i] != c)
		return (0);
	return ((char *)(s + i));
}
